const mongoose = require('mongoose')
const Schema = mongoose.Schema

const producto = new Schema({
  id: {
    type: String,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true,
    unique: true
  },
  value: {
    type: Number,
    required: true,
    min: 0
  },
  amount: {
    type: Number,
    required: true,
    min: 0
  }
}, { timestamps: true })

module.exports = mongoose.model('producto', producto)
